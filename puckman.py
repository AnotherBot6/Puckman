def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Move puckman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(puckman + aim):
        puckman.move(aim)

    index = offset(puckman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(puckman.x + 10, puckman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        if valid(point + course):
            point.move(course)  
        else:
            print(f"El fantasma está en {point.x}, {point.y}")
            print(f"Puckman está en {puckman.x}, {puckman.y}")
            # Checar posicion de puckman con respecto al fantasma
            # Cuadrante I
            if (point.x < puckman.x and point.y < puckman.y):
                options = [
                vector(10, 0),      
                vector(0, 10),
                ]
            # Cuadrante II
            elif (point.x > puckman.x and point.y < puckman.y):
                options = [
                vector(-10, 0),      
                vector(0, 10),
                ]
            # Cuadrante III
            elif (point.x > puckman.x and point.y > puckman.y):
                options = [
                vector(-10, 0),      
                vector(0, -10),
                ]
            # Cuadrante IV
            else:
                options = [
                vector(10, 0),      
                vector(0, -10),
                ]
            # Velocidad de fantasmas aumentado de 5 a 10
                
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(puckman - point) < 20:
            return

    ontimer(move, 100)


def change(x, y):
    """Change puckman aim if valid."""
    if valid(puckman + vector(x, y)):
        aim.x = x
        aim.y = y


